def main():
    parser = argparse.ArgumentParser(
        description="Test Suite Orchestrator (vault-quality framework v2)"
    )
    parser.add_argument("--vault", default=None,
                        help="Vault name (default: active_vault from .claude/state/wikiforge.yaml)")
    parser.add_argument("--lang", default=None,
                        help="Lang code (e.g. en, es)")

    # Subcommands
    parser.add_argument("--generate-questions", action="store_true", dest="generate_questions")
    parser.add_argument("--prepare", action="store_true")
    parser.add_argument("--prepare-evaluator", action="store_true", dest="prepare_evaluator")
    parser.add_argument("--prepare-aggregator", action="store_true", dest="prepare_aggregator")
    parser.add_argument("--consolidate", action="store_true")
    parser.add_argument("--update-tokens", action="store_true", dest="update_tokens",
                        help="Rebuild tokens.json from on-disk artifacts (run after Phase 5 to capture aggregator output)")
    parser.add_argument("--compare", action="store_true")
    parser.add_argument("--check-repetition", action="store_true", dest="check_repetition")
    parser.add_argument("--append-history", action="store_true", dest="append_history",
                        help="Append run summary line to history.md (after consolidate)")

    parser.add_argument("--label", default=None)
    parser.add_argument("--run", type=int, default=None)
    parser.add_argument("--from", dest="from_label", default=None)
    parser.add_argument("--to", dest="to_label", default=None)

    parser.add_argument("--count", type=int, default=None,
                        help=f"Question count (default: vault.yml.tests.count or {DEFAULT_COUNT})")
    parser.add_argument("--mix", default=None,
                        help="Regime mix as A:B:C (default: vault.yml.tests.mix or 5:7:3)")
    parser.add_argument("--evaluators", type=int, default=None,
                        help=f"Number of parallel evaluators (default: vault.yml.tests.evaluators or {DEFAULT_EVALUATORS})")
    parser.add_argument("--regenerate", action="store_true",
                        help="Regenerate questions even if questions.{lang}.yaml exists")
    parser.add_argument("--questions-from", dest="questions_from", default=None,
                        help="Reuse questions from another vault or YAML path "
                             "(e.g. --questions-from oma-test-2 or "
                             "--questions-from path/to/questions.es.yaml). "
                             "Copies into target vault and skips the generator. "
                             "Mutually exclusive with --regenerate.")
    parser.add_argument("--read-queries", action="store_true", dest="read_queries",
                        help="Allow agents/evaluators to read queries/ cache (ablation only — default OFF)")

    args = parser.parse_args()

    _init_vault_config(args.vault)

    # Resolve flags with vault.yml.tests fallback
    count = args.count if args.count is not None else int(_tests_default("count", DEFAULT_COUNT))
    mix = _parse_mix(args.mix or _tests_default("mix", DEFAULT_MIX))
    if sum(mix.values()) != count:
        print(f"[ERR] mix sum ({sum(mix.values())}) != count ({count})")
        sys.exit(1)
    read_queries = args.read_queries or bool(_tests_default("read_queries", False))

    if args.generate_questions:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        generate_questions_prompt(args.label, args.lang, count, mix, args.regenerate,
                                  questions_from=args.questions_from)
    elif args.prepare:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        prepare_prompts(args.label, args.lang, args.run, read_queries)
    elif args.prepare_evaluator:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        prepare_evaluator(args.label, args.lang, args.run, args.evaluators, read_queries)
    elif args.prepare_aggregator:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        prepare_aggregator(args.label, args.lang, args.run)
    elif args.consolidate:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        consolidate_results(args.label, args.lang, args.run)
        if args.append_history:
            results = json.loads((RESULTS_DIR / f"{args.label}.{args.lang}.json").read_text())
            append_history(
                args.label, args.lang, results["run"], results["weighted_avg"],
                results["n_evaluators"], results["n_questions"], results["n_flagged"],
            )
    elif args.update_tokens:
        if not args.label or not args.lang:
            print("[ERR] --label and --lang required")
            sys.exit(1)
        update_tokens(args.label, args.lang, args.run)
    elif args.compare:
        if not args.from_label or not args.to_label or not args.lang:
            print("[ERR] --from, --to, --lang required")
            sys.exit(1)
        compare_results(args.from_label, args.to_label, args.lang)
    elif args.check_repetition:
        if not args.from_label or not args.to_label or not args.lang:
            print("[ERR] --from, --to, --lang required")
            sys.exit(1)
        check_repetition(args.from_label, args.to_label, args.lang)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
