def _delta_pct(before, after):
    if before == 0:
        return 0.0
    return ((after - before) / before) * 100


def compare_results(from_label, to_label, lang):
    from_file = RESULTS_DIR / f"{from_label}.{lang}.json"
    to_file = RESULTS_DIR / f"{to_label}.{lang}.json"
    if not from_file.exists() or not to_file.exists():
        print(f"[ERR] Missing results for {from_label} or {to_label} (lang={lang})")
        sys.exit(1)
    before = json.loads(from_file.read_text())
    after = json.loads(to_file.read_text())

    rows = []
    for d in RUBRIC:
        b = before["dim_aggregate"][d]
        a = after["dim_aggregate"][d]
        rows.append({
            "dim": d,
            "before": b,
            "after": a,
            "delta_abs": round(a - b, 3),
            "delta_pct": round(_delta_pct(b, a), 2),
        })
    wb = before["weighted_avg"]
    wa = after["weighted_avg"]

    comparison = {
        "from": from_label,
        "to": to_label,
        "lang": lang,
        "rubric_version": RUBRIC_VERSION,
        "weighted_avg": {"before": wb, "after": wa, "delta_abs": round(wa - wb, 3)},
        "dims": rows,
        "generated_on": datetime.now(timezone.utc).isoformat(),
    }

    COMPARISONS_DIR.mkdir(parents=True, exist_ok=True)
    out = COMPARISONS_DIR / f"{to_label}-vs-{from_label}.{lang}.json"
    _ensure_under_tests(out)
    out.write_text(json.dumps(comparison, indent=2, ensure_ascii=False))

    print(f"[Compare {from_label} → {to_label} / {lang}]")
    print(f"  weighted_avg: {wb:.2f} → {wa:.2f}  (Δ {wa-wb:+.3f})")
    for row in rows:
        print(f"    {row['dim']:20s}: {row['before']:.2f} → {row['after']:.2f}  "
              f"(Δ {row['delta_abs']:+.2f}, {row['delta_pct']:+.2f}%)")
    print(f"  Saved: {out}")


def check_repetition(from_label, to_label, lang):
    comp_file = COMPARISONS_DIR / f"{to_label}-vs-{from_label}.{lang}.json"
    if not comp_file.exists():
        print(f"[ERR] No comparison file at {comp_file}; run --compare first")
        sys.exit(2)
    comp = json.loads(comp_file.read_text())
    delta = comp["weighted_avg"]["delta_abs"]
    if abs(delta) < REP_COMPOSITE_NOISE * 10:  # 0.15 on the 0-10 scale
        print(f"REPEAT: weighted_avg delta {delta:+.3f} within noise band")
        sys.exit(1)
    print(f"NO_REPEAT: delta {delta:+.3f} is decisive")
    sys.exit(0)


