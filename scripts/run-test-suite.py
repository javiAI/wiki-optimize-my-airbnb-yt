def append_history(label, lang, run, weighted_avg, n_evaluators, n_questions, n_flagged):
    line = (
        f"## [{date.today().isoformat()}] {label} / {lang} / run {run} "
        f"— weighted_avg {weighted_avg:.2f} | {n_evaluators} evaluators × {n_questions} questions | "
        f"{n_flagged} flagged"
    )
    if not HISTORY_FILE.exists():
        HISTORY_FILE.write_text(
            "# Test history\n\nChronological log of /test-vault runs.\n\n"
        )
    with HISTORY_FILE.open("a") as f:
        f.write(line + "\n\n")


