def prepare_prompts(label, lang, run=None, read_queries=False):
    if run is None:
        run = _next_run(label, lang)

    questions_data = load_questions(lang)
    questions = questions_data.get("questions", {})
    if not questions:
        print(f"[ERR] questions.{lang}.yaml has no entries")
        sys.exit(1)

    template = AGENT_TEMPLATE_FILE.read_text()
    run_dir = _run_dir(label, run, lang)
    prompts_subdir = run_dir / "prompts"
    _ensure_under_tests(prompts_subdir)
    prompts_subdir.mkdir(parents=True, exist_ok=True)

    queries_clause = (
        "Reading is permitted because --read-queries was set (ablation only)."
        if read_queries else
        "Reading the queries cache is FORBIDDEN — that is the cache of prior /query answers and would defeat the test."
    )

    for qid, qdata in questions.items():
        output_path = run_dir / f"{qid}.json"
        prompt = template.format(
            qid=qid,
            question=qdata["question"],
            level=qdata.get("level", 1),
            output_path=str(output_path),
            vault_path=VAULT_PATH,
            vault_name=VAULT_NAME,
            repo_path=str(REPO_ROOT),
            lang=lang,
            queries_clause=queries_clause,
        )
        (prompts_subdir / f"{qid}.md").write_text(prompt)

    print(f"[OK] Prepared {len(questions)} prompts for {label} run-{run} lang={lang}")
    print(f"     Prompts dir: {prompts_subdir}")
    print(f"     Outputs: {run_dir}/Q*.json")


