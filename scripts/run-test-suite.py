def _run_dir(label, run, lang):
    return RAW_DIR / label / f"run-{run}" / lang


def _run_root(label, run):
    return RAW_DIR / label / f"run-{run}"


def _iter_run_numbers(label):
    base = RAW_DIR / label
    if not base.exists():
        return
    for p in base.iterdir():
        if not (p.is_dir() and p.name.startswith("run-")):
            continue
        try:
            yield int(p.name.split("-")[1]), p
        except (IndexError, ValueError):
            continue


def _next_run(label, lang=None):
    """Return the next run number to write into.

    If `lang` is given, a run is "occupied" only when it has Q*.json for that
    lang. This lets Phase 2 reuse the same run-N that Phase 1 created prompts
    in (Phase 1 alone leaves an empty Q*.json-less run dir), and lets two
    langs of the same label share the same run number.
    """
    occupied = []
    for n, p in _iter_run_numbers(label):
        if lang is None or list((p / lang).glob("Q*.json")):
            occupied.append(n)
    return (max(occupied) + 1) if occupied else 1


def _existing_runs(label, lang=None):
    runs = []
    for n, p in _iter_run_numbers(label):
        if lang is None or list((p / lang).glob("Q*.json")):
            runs.append(n)
    return sorted(runs)


def _resolve_run(label, run, lang=None):
    if run is not None:
        return run
    runs = _existing_runs(label, lang)
    if not runs:
        suffix = f" lang={lang}" if lang else ""
        print(f"[ERR] No runs found for {label}{suffix}")
        sys.exit(1)
    return runs[-1]


