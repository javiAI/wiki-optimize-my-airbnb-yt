def _estimate_output_tokens(text):
    return int(len(text) / CHARS_PER_TOKEN)


def _build_tokens_summary(run_dir, qids, eval_files, label, lang):
    """Estimate per-stage input/output tokens from on-disk artifacts.

    Method: char-length / CHARS_PER_TOKEN. These are NOT real API counters —
    Claude Code subagents do not expose usage back to the orchestrator. The
    estimates are useful for cross-run comparisons (relative cost trends) but
    absolute values should be read as approximations.

    Schema:
      answers.{qid}, evaluations.{Eid}, aggregator → {input, output, total}
      *_total → per-stage sums
      grand_total → sum across all stages
    """
    def est(s):
        return int(len(s) / CHARS_PER_TOKEN) if s else 0

    answers = {}
    for qid in qids:
        prompt_file = run_dir / "prompts" / f"{qid}.md"
        response_file = run_dir / f"{qid}.json"
        in_tok = est(prompt_file.read_text()) if prompt_file.exists() else 0
        out_tok = 0
        if response_file.exists():
            try:
                resp = json.loads(response_file.read_text())
                out_tok = est(resp.get("response") or "")
            except (json.JSONDecodeError, ValueError):
                pass
        answers[qid] = {
            "input_tokens": in_tok,
            "output_tokens": out_tok,
            "total_tokens": in_tok + out_tok,
        }

    evaluations = {}
    for ef in eval_files:
        eid = ef.stem.replace("evaluation-", "")
        prompt_file = run_dir / "prompts" / f"evaluator-{eid}.md"
        in_tok = est(prompt_file.read_text()) if prompt_file.exists() else 0
        out_tok = est(ef.read_text())
        evaluations[eid] = {
            "input_tokens": in_tok,
            "output_tokens": out_tok,
            "total_tokens": in_tok + out_tok,
        }

    aggregator = None
    agg_prompt = run_dir / "prompts" / "aggregator.md"
    agg_report = REPORTS_DIR / f"{label}.{lang}.md"
    if agg_prompt.exists():
        in_tok = est(agg_prompt.read_text())
        out_tok = est(agg_report.read_text()) if agg_report.exists() else 0
        aggregator = {
            "input_tokens": in_tok,
            "output_tokens": out_tok,
            "total_tokens": in_tok + out_tok,
        }

    def _sum_section(section):
        return {
            "input_tokens": sum(v["input_tokens"] for v in section.values()),
            "output_tokens": sum(v["output_tokens"] for v in section.values()),
            "total_tokens": sum(v["total_tokens"] for v in section.values()),
        }

    answers_total = _sum_section(answers)
    evaluations_total = _sum_section(evaluations)
    agg_total = aggregator if aggregator else {"input_tokens": 0, "output_tokens": 0, "total_tokens": 0}

    return {
        "method": f"char-length / {CHARS_PER_TOKEN} estimate (not real API counters)",
        "answers": answers,
        "answers_total": answers_total,
        "evaluations": evaluations,
        "evaluations_total": evaluations_total,
        "aggregator": aggregator,
        "grand_total": {
            "input_tokens": answers_total["input_tokens"] + evaluations_total["input_tokens"] + agg_total["input_tokens"],
            "output_tokens": answers_total["output_tokens"] + evaluations_total["output_tokens"] + agg_total["output_tokens"],
            "total_tokens": answers_total["total_tokens"] + evaluations_total["total_tokens"] + agg_total["total_tokens"],
        },
    }


def _write_tokens_file(label, lang, run):
    """Build tokens.json from on-disk artifacts and write it."""
    run_dir = _run_dir(label, run, lang)
    if not run_dir.exists():
        print(f"[ERR] Run dir does not exist: {run_dir}")
        sys.exit(1)
    questions_data = load_questions(lang)
    qids = list(questions_data.get("questions", {}).keys())
    eval_files = sorted(run_dir.glob("evaluation-E*.json"))

    summary = _build_tokens_summary(run_dir, qids, eval_files, label, lang)
    tokens_path = run_dir / "tokens.json"
    _ensure_under_tests(tokens_path)
    tokens_path.write_text(json.dumps(summary, indent=2, ensure_ascii=False))
    return tokens_path, summary


def update_tokens(label, lang, run=None):
    run = _resolve_run(label, run, lang)
    tokens_path, summary = _write_tokens_file(label, lang, run)
    gt = summary["grand_total"]
    print(f"[OK] Wrote {tokens_path}")
    print(f"     grand_total: in={gt['input_tokens']:,}  out={gt['output_tokens']:,}  total={gt['total_tokens']:,}")


def _weighted_avg_from_dict(scores):
    return round(sum(scores.get(d, 0) * c["weight"] for d, c in RUBRIC.items()), 3)


def _mean_std(values):
    if not values:
        return 0.0, 0.0
    n = len(values)
    mean = sum(values) / n
    if n < 2:
        return mean, 0.0
    var = sum((v - mean) ** 2 for v in values) / (n - 1)
    return mean, math.sqrt(var)


