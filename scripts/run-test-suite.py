    per_q = {}
    flagged_qs = []
    for qid in qids:
        per_dim = {}
        for d in RUBRIC:
            scores = []
            for ev in evaluators:
                qeval = (ev.get("evaluations") or {}).get(qid, {})
                v = qeval.get(d)
                if v is not None:
                    scores.append(float(v))
            mean, std = _mean_std(scores)
            per_dim[d] = {
                "mean": round(mean, 3),
                "std": round(std, 3),
                "n_evaluators": len(scores),
                "scores": scores,
                "flagged": std > disagreement_threshold,
            }
            if std > disagreement_threshold:
                flagged_qs.append({"qid": qid, "dim": d, "mean": mean, "std": std})

        wavg = _weighted_avg_from_dict({d: per_dim[d]["mean"] for d in RUBRIC})
        per_q[qid] = {
            "level": questions[qid].get("level"),
            "regime": questions[qid].get("regime"),
            "topic": questions[qid].get("topic"),
            "dims": per_dim,
            "weighted_avg": wavg,
        }

    dim_aggregate = {}
    for d in RUBRIC:
        means = [per_q[qid]["dims"][d]["mean"] for qid in qids]
        m, _ = _mean_std(means)
        dim_aggregate[d] = round(m, 3)
    weighted_avg_overall = round(
        sum(dim_aggregate[d] * c["weight"] for d, c in RUBRIC.items()), 3
    )

    # Build tokens.json from on-disk artifacts (input/output split, char-length
    # estimates). Aggregator output may be absent at this point — the report is
    # written by Phase 5. Re-run --update-tokens after Phase 5 to populate it.
    tokens_summary = _build_tokens_summary(run_dir, qids, eval_files, label, lang)
    tokens_path = run_dir / "tokens.json"
    _ensure_under_tests(tokens_path)
    tokens_path.write_text(json.dumps(tokens_summary, indent=2, ensure_ascii=False))

    cost_summary = {
        "method": tokens_summary["method"],
        "answers": tokens_summary["answers_total"],
        "evaluations": tokens_summary["evaluations_total"],
        "aggregator": tokens_summary["aggregator"],
        "grand_total": tokens_summary["grand_total"],
    }

    stats = {
        "label": label,
        "run": run,
        "lang": lang,
        "rubric_version": RUBRIC_VERSION,
        "n_evaluators": len(evaluators),
        "n_questions": len(qids),
        "disagreement_threshold": disagreement_threshold,
        "weighted_avg": weighted_avg_overall,
        "dim_aggregate": dim_aggregate,
        "per_question": per_q,
        "flagged_questions": flagged_qs,
        "cost": cost_summary,
        "generated_on": datetime.now(timezone.utc).isoformat(),
    }

    stats_path = run_dir / "stats.json"
    _ensure_under_tests(stats_path)
    stats_path.write_text(json.dumps(stats, indent=2, ensure_ascii=False))

    # Persist a flatter "results" file at tests/results/{label}.{lang}.json
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    results_path = RESULTS_DIR / f"{label}.{lang}.json"
    _ensure_under_tests(results_path)
    results = {
        "label": label,
        "lang": lang,
        "run": run,
        "rubric_version": RUBRIC_VERSION,
        "weighted_avg": weighted_avg_overall,
        "dim_aggregate": dim_aggregate,
        "n_evaluators": len(evaluators),
        "n_questions": len(qids),
        "n_flagged": len(flagged_qs),
        "thresholds": {"max_dim_disagreement": disagreement_threshold},
        "stats_path": str(stats_path),
        "generated_on": stats["generated_on"],
    }
    results_path.write_text(json.dumps(results, indent=2, ensure_ascii=False))

    print(f"[OK] Consolidated {len(evaluators)} evaluators × {len(qids)} questions")
    print(f"     stats:   {stats_path}")
    print(f"     results: {results_path}")
    print(f"     Weighted avg: {weighted_avg_overall:.2f} / 10")
    for d, m in dim_aggregate.items():
        print(f"       {d:20s} ({RUBRIC[d]['weight']*100:.0f}%): {m:.2f}")
    if flagged_qs:
        print(f"     ⚠ {len(flagged_qs)} (Q,dim) pair(s) above disagreement threshold {disagreement_threshold}")


