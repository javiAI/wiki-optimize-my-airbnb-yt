def _inject_script_word_counts(run_dir, qids):
    """Compute authoritative word count of `response` for each Q*.json and
    write it back as `script_word_count`. Evaluators use this over the agent's
    self-reported `self_assessment.word_count` (which is often missing/wrong).
    """
    updated = 0
    for qid in qids:
        qf = run_dir / f"{qid}.json"
        if not qf.exists():
            continue
        try:
            data = json.loads(qf.read_text())
        except (json.JSONDecodeError, ValueError):
            continue
        response = data.get("response") or ""
        wc = len(response.split())
        if data.get("script_word_count") == wc:
            continue
        data["script_word_count"] = wc
        qf.write_text(json.dumps(data, indent=2, ensure_ascii=False))
        updated += 1
    return updated


def prepare_evaluator(label, lang, run=None, evaluators=None, read_queries=False):
    run = _resolve_run(label, run, lang)
    if evaluators is None:
        evaluators = int(_tests_default("evaluators", DEFAULT_EVALUATORS))

    run_dir = _run_dir(label, run, lang)
    if not run_dir.exists():
        print(f"[ERR] Run dir does not exist: {run_dir}")
        sys.exit(1)

    template = EVALUATOR_TEMPLATE_FILE.read_text()
    questions_data = load_questions(lang)
    n_questions = len(questions_data.get("questions", {}))
    qids = list(questions_data.get("questions", {}).keys())

    n_injected = _inject_script_word_counts(run_dir, qids)
    if n_injected:
        print(f"[OK] Injected script_word_count into {n_injected} Q*.json file(s)")

    response_paths = "\n".join(
        f"   - {run_dir}/Q{i}.json" for i in range(1, n_questions + 1)
    )
    queries_clause = (
        "`{vault_path}/queries/` — permitted because --read-queries was set (ablation only).".replace(
            "{vault_path}", VAULT_PATH
        )
        if read_queries else
        f"`{VAULT_PATH}/queries/` — the cache of prior /query answers; reading would taint the evaluation."
    )

    prompts_dir = run_dir / "prompts"
    prompts_dir.mkdir(parents=True, exist_ok=True)

    for i in range(1, evaluators + 1):
        evaluator_id = f"E{i}"
        output_path = run_dir / f"evaluation-{evaluator_id}.json"
        prompt = template.format(
            label=label,
            run=run,
            lang=lang,
            evaluator_id=evaluator_id,
            evaluator_count=evaluators,
            response_paths=response_paths,
            output_path=str(output_path),
            repo_path=str(REPO_ROOT),
            vault_path=VAULT_PATH,
            rubric_version=RUBRIC_VERSION,
            queries_clause=queries_clause,
            n_questions=n_questions,
        )
        (prompts_dir / f"evaluator-{evaluator_id}.md").write_text(prompt)

    print(f"[OK] Prepared {evaluators} evaluator prompts for {label} run-{run} lang={lang}")
    print(f"     Prompts: {prompts_dir}/evaluator-E*.md")
    print(f"     Outputs: {run_dir}/evaluation-E*.json")


