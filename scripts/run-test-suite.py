"""
Test Suite Orchestrator (vault-quality framework v2)

Per-vault, per-language test framework. Drives the production /query path
through fresh-context subagents, fans out evaluation to N independent
evaluators, computes inter-rater statistics, and produces a narrative report.

Layout produced under vaults/{name}/tests/:

    questions.{lang}.yaml                              # generated by Phase 1
    raw-responses/{label}/run-N/{lang}/
        prompts/Q*.md                                  # answering-agent prompts
        prompts/evaluator-E{i}.md                      # N evaluator prompts
        prompts/aggregator.md                          # 1 aggregator prompt
        Q*.json                                        # answering-agent outputs
        evaluation-E{i}.json                           # N evaluator outputs
        tokens.json                                    # cost telemetry
        stats.json                                     # per-Q mean+std across evals
    results/{label}.{lang}.json                        # consolidated, IRR-aware
    reports/{label}.{lang}.md                          # aggregator narrative
    history.md                                         # chronological log

Subcommands:

    --generate-questions  --label LABEL --lang LANG [--count N] [--mix A:B:C] [--regenerate] [--questions-from VAULT|PATH]
    --prepare             --label LABEL --lang LANG [--run N] [--read-queries]
    --prepare-evaluator   --label LABEL --lang LANG [--run N] [--evaluators N] [--read-queries]
    --prepare-aggregator  --label LABEL --lang LANG [--run N]
    --consolidate         --label LABEL --lang LANG [--run N]
    --update-tokens       --label LABEL --lang LANG [--run N]
    --compare             --from FROM --to TO --lang LANG
    --check-repetition    --from FROM --to TO --lang LANG

ALL test artifacts live INSIDE THIS REPO under vaults/{name}/tests/. The vault
is read-only context for agents; agents NEVER write to vault data.
"""

